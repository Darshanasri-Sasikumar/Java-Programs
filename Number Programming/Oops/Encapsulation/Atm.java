public class Atm {
    private int pin;
    private String name;
    private int slipcount;

    public int getpin(){
        return pin;
    }

    public void setpin(int pin){
        this.pin = pin;
        
    }

    public String getname(){
        return name;
    }

    public void setname(String name){
        this.name = name;
    }

    public int getslipcount(){
        return slipcount;
    }

    public void setslipcount(int slipcount){
        this.slipcount = slipcount;
    }

    
}
